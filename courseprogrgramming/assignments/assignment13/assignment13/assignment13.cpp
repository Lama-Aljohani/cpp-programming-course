#include <iostream>
using namespace std;



int main()
{

    int A;
    int B;



    cout << "Please enter the first number A" << endl;
    cin >> A;
    cout << "Please enter the first number B" << endl;
    cin >> B;

    cout << "A+B=" << A + B << endl;
    cout << "A-B=" << A - B << endl;
    cout << "A*B=" << A * B << endl;
    cout << "A/B=" << A / B << endl;
    cout << "A%B=" << A % B << endl;


    return 0;

}